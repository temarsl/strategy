public class RedheadDuck extends Duck {
    @Override
    public String swim() {
        return "swim";
    }

    @Override
    public String quack() {
        return "quack-quack";
    }

    @Override
    public String fly() {
        return "fly-fly";
    }

    @Override
    public void display() {
        System.out.println("I'm Redhead Duck!");
    }
}
